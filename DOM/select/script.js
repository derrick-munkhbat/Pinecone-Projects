// 1. Use getElementById to select an element by ID
const myElementID = document.getElementById('myElementID');
console.log(myElementID);

// 2. Use getElementsByName to select elements by name
const myElementNames = document.getElementsByName('username');
console.log(myElementNames);

// 3. Use getElementsByTagName to select elements by tag name
const myElementTags = document.getElementsByTagName('ul');
console.log(myElementTags);

// 4. Use getElementsByClassName to select elements by class name
const myElementClass = document.getElementsByClassName('item');
console.log(myElementClass);
